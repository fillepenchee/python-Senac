a = int(input("Digite o valor de A: "))
b = int(input("Digite o valor de B: "))

print(a + b) # adição
print(a - b) # subtração
print(a / b) # divisão
print(a * b) # multiplicação
print(a // b) # divisão inteira (descarta o decimal)
print(a % b) # módulo (ficamos só com o resto)
print(a ** b) # exponenciação


# O resultado sempre será booleano (verdadeiro ou falso)
print(a > b) # maior que
print(a < b) # menor que
print(a >= b) # maior ou igual que
print(a <= b) # menor ou igual que
print(a == b) # igual a
print(a != b) # diferente de

print(a is b) # Idêntico, Identidade (equivalente ao === em outras linguagens)
